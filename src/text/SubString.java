package text;

public class SubString {
	public static void main(String[] args)
	{
		String str1 = "hello";
		String str2 = "yep";
		String str3 = "yessirIsaid";
		String str4 = "el cucuy";
		
		System.out.println(str1.substring(5));
		System.out.println(str2.substring(3));
		System.out.println(str3.substring(11));
		System.out.println(str4.substring(8));
	}
}
